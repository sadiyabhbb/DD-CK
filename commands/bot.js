        // ‡¶è‡¶ü‡¶ø index.js ‡¶è‡¶∞ handleMessage ‡¶è ‡¶•‡¶æ‡¶ï‡¶≤‡ßá ‡¶≠‡¶æ‡¶≤‡ßã, ‡¶§‡¶¨‡ßá ‡¶è‡¶ñ‡¶æ‡¶®‡ßá‡¶ì ‡¶™‡¶∞‡¶ø‡¶∑‡ßç‡¶ï‡¶æ‡¶∞ ‡¶ï‡¶∞‡¶æ ‡¶Ø‡ßá‡¶§‡ßá ‡¶™‡¶æ‡¶∞‡ßá‡•§
        
